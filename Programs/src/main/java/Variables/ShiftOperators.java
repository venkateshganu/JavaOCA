package Variables;

public class ShiftOperators {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a>>3);
        System.out.println(a<<3);
        System.out.println(a>>>3);
        System.out.println(a>>33);
        System.out.println(a<<33);
        System.out.println(a>>>33);

        int b =-10;
        System.out.println(b>>3);
        System.out.println(b<<3);
        System.out.println(b>>>3);
        System.out.println(b>>33);
        System.out.println(b<<33);
        System.out.println(b>>>33);
    }
}
